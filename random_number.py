# using random.randint()
import random 
num = random.randint(1,6)
print(num)


# using random.choice([])
import random
num = random.choice([1, 2, 3, 4, 5, 6])
print(num)


# using random.randrange()
import random
num = random.randrange(1,7) # The upper limit (7) is exclusive, so it goes up to 6.
print(num)
